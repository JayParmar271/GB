package com.register_login;

public class GetsSets{
	
	public String firstname;
	public String lastname;
	public String email;
	public String profile;
	public String mobile;
	public String age;
	public String gender;
	public String address;
	public String state;
	public String password;
	
	
	
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getProfile() {
		return profile;
	}
	public void setProfile(String profile) {
		this.profile = profile;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	@Override
	public String toString() {
		return "GetsSets [firstname=" + firstname + ", lastname=" + lastname
				+ ", email=" + email + ", profile=" + profile + ", mobile="
				+ mobile + ", age=" + age + ", gender=" + gender + ", address="
				+ address + ", state=" + state + ", password=" + password + "]";
	}
	
}
